#escape sequence in string
e="hello\nworld"  #printing in new line
print(e) #o/p:hello
         #world
f="hello\tworld" #printing with tab space
print(f) #o/p:hello  world
g="hello\\world" #printing backslash
print(g) #o/p:hello\world
h="he said,\"hello world\"" #printing double quotes
print(h) #o/p:he said,"hello world"
i='he said,\'hello world\'' #printing single quotes
print(i) #o/p:he said,'hello world'
j="hello\rworld" #carriage return
print(j) #o/p:world